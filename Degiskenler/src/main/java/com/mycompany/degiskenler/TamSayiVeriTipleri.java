package com.mycompany.degiskenler;
public class TamSayiVeriTipleri {
    public static void main(String args[]){
        // Tam Sayıları Gösteren 4 Adet Veri Tipi Vardır Bunlar Byte Short Integer Ve Long Veri Tipleri Vardır.
        // byte < short < int < long
        System.out.println("Byte Max Value && Min Value:" + Byte.MAX_VALUE + " --- " + Byte.MIN_VALUE);
        System.out.println("Short Max Value && Min Value:" + Short.MAX_VALUE + " --- " + Short.MIN_VALUE);
        System.out.println("Integer Max Value && Min Value:" + Integer.MAX_VALUE + " --- " + Integer.MIN_VALUE);
        System.out.println("Long Max Value && Min Value:" + Long.MAX_VALUE + " --- " + Long.MIN_VALUE);
    }
}
